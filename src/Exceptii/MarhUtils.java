package Exceptii;

public class MarhUtils {
    public static int sum(int x, int y){
        return x+y;
    }

    /**
     * Metoda pentru a efectua scaderea a doua nr
     * @param x
     * @param y
     * @return x-y
     */
    public static int scadere(int x, int y){
        return x-y;
    }
}
